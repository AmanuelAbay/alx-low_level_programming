#include <stdio.h>
#include "holberton.h"
/**
 * print_alphabet_x10 - prints alphabet in lowercase 10 times
 * followed by a new line
 *
 * Return: Void
 */
void print_alphabet_x10(void)
{
int count = 1;
char alphabet = 'a';  
while (count <= 10)
{
while (alphabet <= 'z')
{
_putchar(alphabet);
alphabet++;
}
_putchar('\n');
count++;
}
}
